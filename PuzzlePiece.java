package project;

/**
/* The class PuzzlePiece
*/
public class PuzzlePiece
{
    
    public int x = 100;
    public int y = 100;
    
    /**
    /* Default constructor
    */
    public PuzzlePiece()
    {

    }
    
    public int getX() {
        return this.x;
    }
    
    public int getY() {
        return this.y;
    }
    
    public void setX(int x) {
        this.x = x;
    }
    public void setY(int y) {
        this.y = y;
    }
}